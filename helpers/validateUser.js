// ============================================================
// helpers/validateUser.js - Validation Δεδομένων Χρήστη
// ============================================================
// Ελέγχει αν τα δεδομένα που στέλνει το frontend είναι έγκυρα
// ΠΡΙΝ προσπαθήσουμε να τα αποθηκεύσουμε στη βάση.
//
// Γιατί ξεχωριστό αρχείο;
//   Το validation χρησιμοποιείται σε ΔΥΟ routes (POST και PUT).
//   Αν το είχαμε inline, θα το γράφαμε δύο φορές. Εδώ το
//   γράφουμε μία φορά και το κάνουμε import όπου χρειάζεται.
//   Επίσης αν προσθέσουμε νέους κανόνες (π.χ. ελάχιστο μήκος
//   ονόματος), αλλάζουμε ΜΟΝΟ αυτό το αρχείο.
//
// Παράμετροι:
//   data: { name, age, email }
//
// Επιστρέφει:
//   Array με τα μηνύματα σφάλματος (κενό array = όλα εντάξει)
// ============================================================

function validateUser(data) {
  const errors = [];

  // --- Έλεγχος Ονόματος ---
  // Το trim() αφαιρεί κενά από αρχή/τέλος, οπότε "   " θεωρείται κενό
  if (!data.name || data.name.trim() === "") {
    errors.push("Το όνομα είναι υποχρεωτικό.");
  }

  // --- Έλεγχος Ηλικίας ---
  // Ελέγχουμε πρώτα αν υπάρχει τιμή, μετά αν είναι έγκυρος αριθμός
  if (data.age === undefined || data.age === null || data.age === "") {
    errors.push("Η ηλικία είναι υποχρεωτική.");
  } else if (
    isNaN(data.age) ||
    Number(data.age) < 0 ||
    Number(data.age) > 120
  ) {
    errors.push("Η ηλικία πρέπει να είναι αριθμός μεταξύ 0 και 120.");
  }

  // --- Έλεγχος Email ---
  // Regex για βασικό έλεγχο μορφής email (κάτι@κάτι.κάτι)
  if (!data.email || data.email.trim() === "") {
    errors.push("Το email είναι υποχρεωτικό.");
  } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(data.email)) {
    errors.push("Το email δεν έχει έγκυρη μορφή.");
  }

  // Αν το array είναι κενό → δεν υπάρχουν σφάλματα → τα δεδομένα είναι έγκυρα
  return errors;
}

export { validateUser };
